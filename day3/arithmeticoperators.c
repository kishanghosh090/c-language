#include <stdio.h>
#include<math.h>
int main()
{
    int a = 4;
    int b = 3;
    int c = a + b;
    printf("the value of a is %d and value of b is %d and sum is %d", a, b, c);
    printf("\n");
    // modulus operator is used to get remainder
    printf("the remainder is %d", a % b);
    return 0;
}