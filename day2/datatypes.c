#include <stdio.h>

// data typess in c language

int main(){
    int a = 1;
    float b = 3.2;
    char c = 'a';
    double d = 3.2;
    printf("sizeof int : %d bytes\n", sizeof(a));
    printf("sizeof char : %d bytes\n", sizeof(b));
    printf("sizeof float : %d bytes\n", sizeof(c));
    printf("sizeof double : %d bytes\n", sizeof(d));
    printf("this is end of program \n");
    // sizeof int : 4 bytes sizeof char : 1 bytes sizeof float : 4 bytes
    return 0;
}





















